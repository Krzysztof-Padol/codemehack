// funkcja tworzy pojedynczy artykul ze zdjeciem, nazwa i czasem z rokiem
function createArticle(name="",imgSrc,year=""){
    let articleContainer = document.createElement('article');
    let image = document.createElement('img');
    let footer = document.createElement('footer');
    let spanName = document.createElement('span');
    let spanYear = document.createElement('span');
    
    articleContainer.classList.add('col-lg-4','col-12');

    image.src = imgSrc;
    image.alt = "some photo";
    image.classList.add('article-image');

    spanName.classList.add('name');
    spanYear.classList.add('year');

    spanName.innerHTML = name;
    spanYear.innerHTML = year;

    footer.appendChild(spanName);
    footer.appendChild(spanYear);

    articleContainer.appendChild(image);
    articleContainer.appendChild(footer);

    return articleContainer
};

// fukncja iteruje po bazie danych(data) i tworzy daną ilość(howMany) artykulow ze zdjęciem w sekcji,
// w tym celu trzewa w forze wywołac funkcje z góry
function createArticlesSection(data,howMany,destination){
    for(i=0;i<howMany;i++){
        var article = createArticle('',
            // data[i].title, // data[i].name
            data[i].img,''
            // data[i].release_year, // ''
        )
        destination.appendChild(article);
    }

};

// funkcja wykorzystuje funckje nad nami i tworzy calą sekcje , ktorej wygląd zależy czy mamy do czynienia z aktorami czy filmami
function createSectionMainContainer(buttonName,headerName,typeOfData,howManyArticles){
    let mainContent = document.createElement('div');
    let row = document.createElement('div');
    let col = document.createElement('div');
    let header = document.createElement('h2');
    let section = document.createElement('section');
    let button = document.createElement('button');


    // dodaje klasy do zmiennych
    mainContent.classList.add('container','main-content');

    row.classList.add('row');
    col.classList.add('col-12');

    section.classList.add('col-12','d-flex','justify-content-around');

    // zaleznie czy mamy do czynienia z aktorami czy filmami dodajemy takie id do buttona
    if(typeOfData === actors){
        button.classList.add("actorsId");
    }else if (typeOfData === movies){
        button.classList.add("moviesId");
    }

    button.classList.add('btn','seeAll-button','btn-default');

    // dodaje inne shity do zmiennych
    button.type = 'button';
    button.innerHTML = buttonName;
    header.innerHTML = headerName;

    // dodaje artykuly -> sekcji , i cała reszte w kolejnosci od góry do głownego ckontenera
    createArticlesSection(typeOfData,howManyArticles,section);

    col.appendChild(header);
    col.appendChild(section);
    col.appendChild(button);

    row.appendChild(col);
    mainContent.appendChild(row);

    return mainContent
};


